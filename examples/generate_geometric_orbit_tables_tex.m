    print "\\midrule";
    print "\\multicolumn{4}{r}{Continued on next page}\\\\";
    print "\\midrule";
    print "\\endfoot";
    print "\\bottomrule";
    print "\\endlastfoot";

    for i in [1..#data] do
        print Sprint(i) cat " & "
