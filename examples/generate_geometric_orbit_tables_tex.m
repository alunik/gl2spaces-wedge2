ReplaceAllStrings := function(s, old, new)
    if old eq "" then
        return s;
    end if;

    out := "";
    rest := s;
    pos := Position(rest, old);
    while pos ne 0 do
        if pos gt 1 then
            out cat:= rest[1..pos - 1];
        end if;
        out cat:= new;
        start := pos + #old;
        if start le #rest then
            rest := rest[start..#rest];
            pos := Position(rest, old);
        else
            rest := "";
            pos := 0;
        end if;
    end while;

    return out cat rest;
end function;

IsDigitChar := function(c)
    return c ge "0" and c le "9";
end function;

ReadDigitToken := function(s, i)
    j := i;
    while j le #s and IsDigitChar(s[j]) do
        j +:= 1;
    end while;
    return s[i..j - 1], j;
end function;

BraceScriptsInLatexMath := function(s)
    out := "";
    i := 1;
    while i le #s do
        c := s[i];
        if (c eq "_" or c eq "^") and i lt #s and s[i + 1] ne "{" then
            out cat:= c cat "{";
            i +:= 1;
            while i le #s and (IsDigitChar(s[i]) or (s[i] ge "a" and s[i] le "z") or
                (s[i] ge "A" and s[i] le "Z")) do
                out cat:= s[i];
                i +:= 1;
            end while;
            out cat:= "}";
            continue;
        end if;
        out cat:= c;
        i +:= 1;
    end while;
    return out;
end function;

LatexTypeString := function(s)
    return "$" cat BraceScriptsInLatexMath(s) cat "$";
end function;

LatexWedgeString := function(s)
    out := "";
    i := 1;
    while i le #s do
        if s[i] eq "e" and i lt #s and IsDigitChar(s[i + 1]) then
            digits, j := ReadDigitToken(s, i + 1);
            out cat:= "e_{" cat digits cat "}";
            i := j;
            continue;
        end if;

        if s[i] eq "^" then
            out cat:= " \\wedge ";
            i +:= 1;
            continue;
        end if;

        out cat:= s[i];
        i +:= 1;
    end while;

    return "$" cat out cat "$";
end function;

